export const getDailyQuestionIndex = (coupleId, dateStr) => {
  let hash = 0;
  const seed = coupleId + dateStr;
  for (let i = 0; i < seed.length; i++) {
    const char = seed.charCodeAt(i);
    hash = ((hash << 5) - hash) + char;
    hash = hash & hash;
  }
  return Math.abs(hash) % DAILY_QUESTIONS.length;
};
